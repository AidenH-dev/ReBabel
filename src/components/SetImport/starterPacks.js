export function getStarterSetItems(setKey) {
  if (setKey === 'common_words') {
    return COMMON_WORDS_DATA.map((w) => ({
      primary: w.kanji || w.kana,
      secondary: w.kanji ? w.kana : '',
      english: w.english,
    }));
  }
  if (setKey === 'hiragana') {
    return HIRAGANA_DATA.map((c) => ({
      primary: c.char,
      secondary: '',
      english: c.romaji,
    }));
  }
  if (setKey === 'katakana') {
    return KATAKANA_DATA.map((c) => ({
      primary: c.char,
      secondary: '',
      english: c.romaji,
    }));
  }
  return [];
}

export function buildStarterSetPayload(setKey, userId) {
  const currentDate = new Date().toISOString();

  const buildItems = (data, type) =>
    data.map((item) => ({
      owner: userId,
      type: 'vocab',
      english: type === 'char' ? item.romaji : item.english,
      kana: type === 'char' ? item.char : item.kana,
      kanji: type === 'char' ? '' : item.kanji,
      lexical_category: type === 'char' ? 'character' : item.category,
      example_sentences: '',
      tags:
        type === 'char'
          ? [type === 'char' ? setKey : 'common', 'character']
          : ['common', 'essential'],
      audio: '',
      known_status: 'unknown',
      srs_level: 0,
      srs_reviewed_last: null,
    }));

  const meta = STARTER_SETS.find((s) => s.key === setKey);
  if (!meta) return null;

  let items;
  if (setKey === 'common_words') {
    items = buildItems(COMMON_WORDS_DATA, 'word');
  } else if (setKey === 'hiragana') {
    items = buildItems(HIRAGANA_DATA, 'char');
  } else {
    items = buildItems(KATAKANA_DATA, 'char');
  }

  return {
    set: {
      owner: userId,
      title: meta.title,
      description: meta.description,
      date_created: currentDate,
      updated_at: currentDate,
      last_studied: currentDate,
      tags: meta.tags,
    },
    items,
  };
}
